package settimana2.giorno3.esercizio2;

public class CategoriaMerceologica {
	
	private String nome;
	private String codice;
	
	public CategoriaMerceologica(String nome, String codice) {
		this.nome = nome;
		this.codice = codice;
	}

	public String getNome() {
		return nome;
	}

	public String getCodice() {
		return codice;
	}

}
